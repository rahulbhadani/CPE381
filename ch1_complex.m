%% Computing using complex number

% print the imaginary unit
i
j

% define complex number
a = 3 + j*4
b = 6 + j*9

% retrieve real part
real(a)

% retrieve imaginary part, put semicolon at the end to suppress output
imag(b);

% save output the variable
c = imag(b);
disp(c) % display

% conjugate of a
conj(a)

% get the angle in polar co-cordinate
angle(a)

% get the absolute value
abs(a)


% addition
a + b

% division
a/b